    private void searchPaymentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPaymentBtnActionPerformed
       try {
            PaymentD paymentD=PaymentDController.searchPaymentD(pIDTxt.getText());
            if(paymentD!=null){
                vNumTxt1.setText(paymentD.getvNum());
                vTypeTxt.setText(paymentD.getvehicleType());
                dateTxt.setText(paymentD.getDate());
                aTimeTxt.setText(paymentD.getarrivalTime());
                dTimeTxt.setText(paymentD.getdepartureTime());
                priceTxt.setText(paymentD.getPrice()+"");
                               
            }else{
                JOptionPane.showMessageDialog(this, "Vehicle not found...");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Driver not found...");
        }      // TODO add your handling code here:
    }//GEN-LAST:event_searchPaymentBtnActionPerformed

