    private void cardButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cardButtonActionPerformed
        try {
            PaymentD paymentD=new PaymentD(Integer.parseInt(pIDTxt.getText()));
            boolean isUpdated=PaymentDController.updatePaymentDcard(paymentD);
            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Card Payment Success");
            } else {
                JOptionPane.showMessageDialog(this, "Card Payment Fail");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        } // TODO add your handling code here:
    }//GEN-LAST:event_cardButtonActionPerformed

