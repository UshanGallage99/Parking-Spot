    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
            CustomerD customerD=CustomerDController.searchCustomerD(vNumTxt.getText());
            if(customerD!=null){
                nameTxt1.setText(customerD.getOwnerName());
                contactTxt.setText(customerD.getContact()+"");
            }else{
                JOptionPane.showMessageDialog(this, "Vehicle not found...");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Driver not found...");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_searchBtnActionPerformed

